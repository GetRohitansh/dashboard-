function convertFormat(data) {
    const result = {};
    for (const state in data) {
      const disabilities = [];
      for (const disability in data[state]) {
        const { male_count, female_count } = data[state][disability];
        disabilities.push({
          name: disability,
          male_count: male_count,
          female_count: female_count
        });
      }
      result[state] = disabilities;
    }
    return result;
  }
  
  

